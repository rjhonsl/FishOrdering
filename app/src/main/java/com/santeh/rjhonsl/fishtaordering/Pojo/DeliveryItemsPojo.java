public class DeliveryItemsPojo {

    private String id;
    private String storeID;
    private String drNumber;
    private String actualItemID;
    private String actualQty;
    private String actualunits;
    private String receidItemid;
    private String receivedQty;
    private String receivedUnits;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    public String getStoreID() {
        return storeID;
    }

    public void setStoreID(String storeID) {
        this.storeID = storeID;
    }

    public String getDrNumber() {
        return drNumber;
    }

    public void setDrNumber(String drNumber) {
        this.drNumber = drNumber;
    }


    public String getActualItemID() {
        return actualItemID;
    }

    public void setActualItemID(String actualItemID) {
        this.actualItemID = actualItemID;
    }

    public String getActualQty() {
        return actualQty;
    }

    public void setActualQty(String actualQty) {
        this.actualQty = actualQty;
    }

    public String getActualunits() {
        return actualunits;
    }

    public void setActualunits(String actualunits) {
        this.actualunits = actualunits;
    }

    public String getReceidItemid() {
        return receidItemid;
    }

    public void setReceidItemid(String receidItemid) {
        this.receidItemid = receidItemid;
    }

    public String getReceivedQty() {
        return receivedQty;
    }

    public void setReceivedQty(String receivedQty) {
        this.receivedQty = receivedQty;
    }

    public String getReceivedUnits() {
        return receivedUnits;
    }

    public void setReceivedUnits(String receivedUnits) {
        this.receivedUnits = receivedUnits;
    }
}
